package com.bptn.course_05_strings;

public class PrintEvenFrom0To100 {

	public static void main(String[] args) {
		// start from 2 (the first even number)
		int evenNumber = 2;
		
		// loop until number is 100
		while (evenNumber <= 100) {
			System.out.println(evenNumber);
			evenNumber += 2;
			
			
		}

	}

}
